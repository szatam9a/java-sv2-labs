package math.math;

import java.sql.SQLOutput;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(5,6));
        System.out.println(Math.min(5,6));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(33.66));
        System.out.println(Math.abs(-25));
        System.out.println(Math.negateExact(-25));
        System.out.println(Math.addExact(3,4));
        System.out.println(Math.subtractExact(3,4));
        System.out.println(Math.pow(0.34,0.56));
        System.out.println(Math.incrementExact(2));
        System.out.println(Math.decrementExact(2));
        System.out.println(Math.random());

    }
}
